// تابع برای تشخیص رنگ سفید
const isWhiteColor = (color: string): boolean => {
  if (!color || color === "transparent") return false
  const hex = color.replace("#", "").toLowerCase()
  return hex === "ffffff" || hex === "fff" || color.toLowerCase() === "white"
}

